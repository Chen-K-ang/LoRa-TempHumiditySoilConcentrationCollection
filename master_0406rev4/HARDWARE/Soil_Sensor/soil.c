void Soil_ValAnalysis(SOIL_TypeDef *soil)
{
	u8 len=0;
	u16 CRC_Check = 0;
	int data = 0;
#ifdef SOIL_DEBUG
	SOIL_RxTest();
#endif
	if((USART2_RX_STA&0X8000)!=0)
	{
		len = USART2_RX_STA&0X3FFF;
		CRC_Check = CRC_Compute(USART2_RX_BUF,len-2);
#ifdef SOIL_DEBUG
		UsartPrintf(USART_DEBUG,"CRC_H:%2x CRC_L:%2x \r\n",CRC_Check>>8,CRC_Check&0X00FF);
#endif
		if(USART2_RX_BUF[len-1]==(CRC_Check&0X00FF))//验证CRC高位
		{
#ifdef SOIL_DEBUG
			UsartPrintf(USART_DEBUG,"CRC 高位验证成功\r\n");
#endif
			if(USART2_RX_BUF[len-2]==(CRC_Check>>8))//验证CRC高位
			{
#ifdef SOIL_DEBUG
				UsartPrintf(USART_DEBUG,"CRC 低位验证成功\r\n");
#endif
				data = (USART2_RX_BUF[3]<<8)+USART2_RX_BUF[4];
				soil->humidity = (float)data/10.0;
				data = (USART2_RX_BUF[5]<<8)+USART2_RX_BUF[6];
				soil->temp = (float)data/10.0;
				data = (USART2_RX_BUF[9]<<8)+USART2_RX_BUF[10];
				soil->ph_val = (float)data/10.0;
				
				data = (USART2_RX_BUF[11]<<8)+USART2_RX_BUF[12];
				soil->N_val = (float)data/10.0;
				data = (USART2_RX_BUF[13]<<8)+USART2_RX_BUF[14];
				soil->P_val = (float)data/10.0;
				data = (USART2_RX_BUF[15]<<8)+USART2_RX_BUF[16];
				soil->K_val = (float)data/10.0;
			}
			else
			{
#ifdef SOIL_DEBUG
				UsartPrintf(USART_DEBUG,"CRC 低位验证失败\r\n");
#endif	
			}
		}
		else
		{
#ifdef SOIL_DEBUG
			UsartPrintf(USART_DEBUG,"CRC 高位验证失败\r\n");
#endif	
		}
		USART2_RX_STA=0;
	}
}

void SOIL_GetOneData(SOIL_TypeDef *soil)
{
	while(1)//获取湿度数据
	{
		Soil_SendCommand(SOIL_SensorADDR,0X03,HUMIDITY_REG,1);
		delay_ms(5);
		if(USART2_RX_STA&0X8000)break;
	}
	USART2_RX_STA=0;
}





